package com.emis.util;

public class Search {

	/**
	 * 当前页数
	 */
	private int page ;
	
	/**
	 * 每页数据数
	 */
	private int rows ;

	public int getPage() {
		return page;
	}

	public void setPage(int page) {
		this.page = page;
	}

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}
	
	
}
